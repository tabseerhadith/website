  <p>
    <em>Jāmiʿ at-Tirmidhī</em> is a collection of ḥadīth compiled by Imām Abū ʿĪsā Muḥammad at-Tirmidhī (raḥimahullāh).  
    His collection is unanimously recognized as one of the six canonical ḥadīth compilations 
    (<em>Kutub as-Sittah</em>) of the Sunnah of the Prophet (ﷺ).  
    It contains approximately 4,400 ḥadīth (with repetitions) organized into 46 books.
  </p>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">Author Bio</h2>
    <p>
      <!-- Instead of PHP include -->
      Imām at-Tirmidhī was born in 209 AH (824 CE) in the city of Tirmidh (in modern-day Uzbekistan).  
      A student of Imām al-Bukhārī, he also studied under Imām Muslim and Imām Abū Dāwūd, while some 
      scholars — including Imām al-Bukhārī himself — benefited from him in turn.  
      He was known for his sharp intellect, precision in ḥadīth criticism, and immense piety.
    </p>

    <p>
      His memory and scholarship were unmatched — it is narrated that he lost his eyesight later in life 
      due to constant weeping out of khashya (awe of Allah).  
      Imām al-Bukhārī once told him: “I have benefited from you more than you have benefited from me.”  
      His <em>Jāmiʿ</em> was presented to the scholars of Ḥijāz, ʿIrāq, and Khurāsān, and was met with unanimous approval.
    </p>
  </section>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">About the Jāmiʿ</h2>
    <p>
      This collection is formally titled:  
      <em>Al-Jāmiʿ al-Mukhtaṣar min as-Sunan ʿan Rasūlillāh ﷺ wa Maʿrifatu aṣ-Ṣaḥīḥ wal-Maʿlūl wa mā ʿalayhi al-ʿAmal</em>,  
      meaning “The Concise Compendium of the Sunnah of the Messenger of Allah ﷺ, identifying the authentic and defective, 
      and what is acted upon.”
    </p>
  </section>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">Methods of Classification and Annotation</h2>
    <p>
      According to the commentators of <em>al-Jāmiʿ</em>, Imām at-Tirmidhī adhered to several core principles in his compilation:
    </p>

    <ol class="list-decimal pl-6">
      <li>He never narrated from known fabricators of ḥadīth.</li>
      <li>
        As noted by Ṭāhir al-Muqaddisī, the <em>Jāmiʿ</em> contains four categories of narrations:
        <ul class="list-disc pl-6">
          <li>Those meeting the criteria of al-Bukhārī and Muslim.</li>
          <li>Those conforming to the standards of Abū Dāwūd and an-Nasāʾī.</li>
          <li>Those with minor discrepancies in chain (<em>sanad</em>) or text (<em>matn</em>).</li>
          <li>Weak narrations relied upon by jurists (<em>fuqahāʾ</em>).</li>
        </ul>
      </li>
      <li>He accepted a narration reported with <em>ʿan</em> (indirect transmission) if both narrators were contemporaries.</li>
      <li>After mentioning a weak narration, he clarified its weakness explicitly.</li>
      <li>He accepted <em>mursal</em> (interrupted) narrations if supported by connected chains.</li>
    </ol>

    <p>
      <em>Jāmiʿ at-Tirmidhī</em> holds a unique status among the six canonical collections.  
      Most scholars rank it fifth in authenticity after <em>Bukhārī</em>, <em>Muslim</em>, <em>Abū Dāwūd</em>, and <em>an-Nasāʾī</em>.  
      However, others — such as al-Dhahabī and Ḥājjī Khalīfah — considered it to merit third place due to its 
      organization, methodology, and clarity.
    </p>
  </section>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">Special Characteristics of Jāmiʿ at-Tirmidhī</h2>
    <ol class="list-decimal pl-6">
      <li>It is both a <em>Sunan</em> (legal compilation) and a <em>Jāmiʿ</em> (comprehensive collection).</li>
      <li>Only 83 ḥadīth are repeated throughout the work.</li>
      <li>Imām at-Tirmidhī often quoted only the relevant part of a long ḥadīth under each chapter title.</li>
      <li>He classified every narration, indicating whether it was authentic, ḥasan, or weak.</li>
      <li>He clarified the identity of narrators, explaining their <em>kunya</em> (honorific) or proper names when needed.</li>
      <li>One narration in the collection is a <em>thulāthiyyāt</em> — with only three narrators between him and the Prophet ﷺ.</li>
      <li>Every ḥadīth in <em>al-Jāmiʿ</em> is <em>maʿmūl bihi</em> — acted upon by jurists.</li>
      <li>He presented various madhāhib (legal schools) along with their evidences.</li>
      <li>He explained complex or ambiguous narrations in clear language.</li>
      <li>The structure of the book is orderly, making it easy to locate narrations.</li>
      <li>No fabricated ḥadīth appear in the entire collection.</li>
    </ol>
  </section>
