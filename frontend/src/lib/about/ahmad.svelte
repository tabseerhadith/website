  <p>
    <em>Musnad Ahmad</em> is a collection of ḥadīth compiled by Imām Aḥmad ibn Ḥanbal (raḥimahullāh). 
    It is regarded as one of the nine principal books of ḥadīth of the Sunnah of the Prophet (ﷺ).
    The collection contains approximately 28,199 aḥādīth organized according to 1,277 Companion narrators.
