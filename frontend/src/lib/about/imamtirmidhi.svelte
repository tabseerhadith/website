    <h2 class="text-lg font-semibold mb-2">Author Bio</h2>
    <p>
      He is Abū ʿĪsā Muḥammad ibn ʿĪsā ibn Sawrah ibn Mūsā ibn aḍ-Ḍaḥḥāk as-Sulamī at-Tirmidhī 
      (209–279 AH / 824–892 CE). Imām at-Tirmidhī was born in 209 AH during the reign of the 
      ʿAbbāsid Caliph al-Maʾmūn ar-Rashīd. The ʿAbbāsid era, though intellectually vibrant, also 
      witnessed the influx of Greek philosophy into the Islamic world. This led to the rise of the 
      Muʿtazilah school of thought, which was endorsed by the state. Opposing it often meant 
      opposing the government itself.
    </p>

    <p>
      Many Muslims attempted to reconcile reason and revelation, introducing deviations into 
      religion. As a result, numerous scholars emerged to defend the Sharīʿah. During this period, 
      some rulers also forged ḥadīths for political ends. Earlier, Caliph ʿUmar ibn ʿAbd al-ʿAzīz 
      (raḥimahullāh) had already initiated a systematic effort to compile the Prophet’s ḥadīths to 
      preserve them from being lost. Among the leading scholars who continued this legacy, six 
      stand out most prominently — and one of them was Imām Abū ʿĪsā Muḥammad ibn ʿĪsā at-Tirmidhī.
    </p>

    <p>
      Growing up in an environment of scholarship, Imām at-Tirmidhī dedicated his life to the study 
      and preservation of ḥadīth. After obtaining his foundational education in his hometown, he 
      traveled widely in search of knowledge. He studied under great scholars such as Imām al-Bukhārī, 
      Imām Muslim, and Imām Abū Dāwūd — and in some narrations, Imām al-Bukhārī and Imām Muslim were 
      also his students.
    </p>

    <p>
      Imām al-Bukhārī once told him: “I have benefited more from you than you have benefited from me.”  
      Mūsā ibn ʿAlaq said, “When Imām al-Bukhārī passed away, there remained no one in Khurāsān 
      comparable to Abū ʿĪsā at-Tirmidhī in knowledge, memory, piety, and abstinence.”  
      Imām at-Tirmidhī himself mentioned that after compiling his <em>Jāmiʿ</em>, he presented it to 
      the scholars of Ḥijāz, ʿIrāq, and Khurāsān, and they all approved of it.
    </p>
  </section>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">His Memory</h2>
    <p>
      Imām at-Tirmidhī was gifted with an extraordinary memory. Once, on his way to Makkah, he met 
      a muḥaddith from whom he had previously written two chapters of ḥadīth. Intending to have them 
      reviewed, he realized he did not have his notes and instead recited both chapters from memory 
      on blank paper. The muḥaddith thought he was pretending and scolded him for wasting time.
    </p>

    <p>
      Imām at-Tirmidhī assured him that he had memorized them perfectly. To test him, the scholar 
      recited forty ḥadīths — which Imām at-Tirmidhī repeated flawlessly without a single error. 
      This incident spread his reputation as a master memorizer across the scholarly world.
    </p>
  </section>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">His Works</h2>
    <p>
      Many ḥadīth compilations predated Imām at-Tirmidhī’s <em>Jāmiʿ</em>. Among them were the works of 
      Dāwūd aṭ-Ṭayālisī and Aḥmad ibn Ḥanbal, which contained both authentic and weak narrations. 
      Imām al-Bukhārī then compiled his <em>Ṣaḥīḥ</em>, restricting it to only sound narrations with 
      the goal of deriving legal rulings. Later, Imām Muslim produced his own <em>Ṣaḥīḥ</em> focused 
      on collecting multiple isnāds (chains of narration) for each report.
    </p>

    <p>
      Imām an-Nasāʾī’s compilation emphasized identifying discrepancies in narrators, while 
      Imām Abū Dāwūd’s <em>Sunan</em> served as a foundational legal reference for jurists.  
      Imām at-Tirmidhī uniquely combined the approaches of these masters — mentioning discrepancies 
      in narration, providing commentary on juristic issues, and classifying narrations by strength — 
      making his <em>Jāmiʿ</em> a comprehensive work serving both scholars and students of law.
    </p>
  </section>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">His Students</h2>
    <p>
      Imām at-Tirmidhī taught a large circle of students across the Muslim world. Among his most 
      prominent were Haytham ibn Kulayb, Abul-ʿAbbās, and Muḥammad ibn Aḥmad Shāh ʿAbdul-ʿAzīz.  
      The latter described him as “a man of unmatched memory, deep piety, and immense fear of Allah.  
      He would cry so often out of reverence that he eventually lost his sight.”
    </p>

    <p>
      According to Ibn Taymiyyah and Shāh Walīullāh ad-Dihlawī, Imām at-Tirmidhī was not merely a 
      transmitter of ḥadīth but also an independent jurist (<em>mujtahid</em>), capable of deriving 
      rulings directly from textual evidence.
    </p>
  </section>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">His Death</h2>
    <p>
      In 279 AH (≈ 892 CE), at the age of 70, Imām at-Tirmidhī passed away in the village of 
      Bugh (near Tirmidh). His devotion, humility, and scholarship continue to be revered by 
      scholars and students of ḥadīth to this day.
    </p>
  </section>
