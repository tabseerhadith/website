  <p>
    <em>Sunan Ibn Mājah</em> is a collection of ḥadīth compiled by Imām Muḥammad bin Yazīd Ibn Mājah al-Qazvīnī (raḥimahullāh).  
    It is widely regarded as the sixth of the six canonical ḥadīth collections (<em>Kutub as-Sittah</em>) of the Sunnah of the Prophet (ﷺ).  
    The collection consists of 4,341 aḥādīth in 37 books.
  </p>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">Author Bio</h2>
    <p>
      Abū ʿAbdullāh Muḥammad bin Yazīd bin ʿAbdullāh ar-Rabʿī al-Qazvīnī, known as Ibn Mājah, was born in 209 AH to a non-Arab tribe,
      Rabʿī, in Qazvīn (Iran). Scholars differ about his nickname — some say Mājah was his mother’s name, others that it was his father’s laqab.
    </p>
  </section>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">Travels to Learn Ḥadīth</h2>
    <p>
      Ibn Mājah began studying ḥadīth in his hometown of Qazvīn, which had become a major center of hadith studies.  
      At around 230 AH (age 21 or 22) he travelled through Khurasān, Iraq, Ḥijāz, Egypt and Shām to learn from the scholars of his era.  
      He later reached Baghdād — described by Imām adh-Dhahabī as the “Dār al-isnād al-ʿālī wal-ḥifẓ,” the seat of memorization and chains of transmission — and continued journeys to Damascus, Ḥimṣ, Isfahān, Ashkelon and Nīshāpūr.
    </p>
  </section>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">His Teachers</h2>
    <p>
      In Madinah he studied under Ḥāfiẓ Ibn Muṣʿab az-Zubayrī, Aḥmad bin Abī Bakr al-ʿAwfī, and Ḥāfiẓ Ibrāhīm bin al-Mundhir.  
      His teachers in Makkah included Ḥāfiẓ Jalwānī, Abū Muḥammad Ḥasan bin ʿAlī al-Khilāl, Ḥāfiẓ Zubayr bin Bakkār (the judge of Makkah), and Ḥāfiẓ Salamah bin Shabīb.  
      In Qazvīn he learned from ʿAmr bin Rāfiʿ al-Bajalī, Ismāʿīl bin Tawbah, and Muḥammad bin Abī Khālid al-Qazvīnī.  
      Other eminent teachers included Jubārah bin Mughallis, Abū Bakr bin Abī Shaybah, Naṣr bin ʿAlī an-Nīshāpūrī, and ʿAlī bin Mundhir.
    </p>
  </section>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">His Students</h2>
    <p>
      Ibn Mājah taught many pupils across Qazvīn, Isfahān, Ḥamadhān, Baghdād and elsewhere.  
      Notable students include ʿAlī bin ʿAbdullāh al-Falānī, Ibrāhīm bin Dīnār al-Jarshī, Aḥmad bin Ibrāhīm al-Qazvīnī, Ḥāfiẓ Abū Yaʿlā al-Khalīlī, and Abū ʿAmr Aḥmad bin Muḥammad bin Ḥākim al-Madani al-Iṣfahānī.
    </p>
  </section>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">Rank Among Scholars</h2>
    <p>
      Imām adh-Dhahabī said, “Ibn Mājah memorized aḥādīth by heart, was a critic in Ḥadīth sciences, truthful, upright, and widely learned.”  
      Abū Yaʿlā al-Khalīlī described him as “trustworthy, authoritative, and deeply knowledgeable.”  
      ʿAllāmah Sindī added, “Among the Imāms of Ḥadīth he held a high rank and was pious and reliable by consensus.”
    </p>
  </section>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">Works</h2>
    <p>
      After completing his studies, Imām Ibn Mājah devoted his life to writing and produced three great works:
      <em>as-Sunan</em>, <em>at-Tafsīr</em>, and <em>at-Tārīkh</em>.  
      <em>As-Sunan</em> ranks sixth among the six major ḥadīth books.  
      <em>At-Tafsīr</em> was a Qurʾān commentary built upon ḥadīth and the sayings of Companions and Tābiʿīn,  
      while <em>at-Tārīkh</em> was a historical chronicle demonstrating his vast scholarship — both praised by Ibn Kathīr but now lost.
    </p>
  </section>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">Death</h2>
    <p>
      Imām Abū ʿAbdullāh Muḥammad Ibn Mājah al-Qazvīnī passed away on Monday, 22 Ramaḍān 273 AH (≈ 887 CE) at age 64.  
      The poet Muḥammad bin Aswad al-Qazvīnī lamented:  
      “The loss of Ibn Mājah weakened the column of the throne of knowledge and shook its pillars.”
    </p>
  </section>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">His Sunan</h2>
    <p>
      <em>Sunan Ibn Mājah</em> is arranged primarily by fiqh topics but also covers ʿAqīdah, dream interpretation,
      tribulations, and asceticism.  
      When Imām Abū Zurʿah ar-Rāzī examined it, he said,  
      “If this book reaches the public, most other compilations will no longer be used.”  
      This prophecy proved true as Ibn Mājah’s work eventually became the sixth of the Six.
    </p>

    <p>
      The collection contains 37 books, 1,560 chapters and 4,341 aḥādīth, including 1,339 additional narrations
      (<em>Zawāʾid</em>) not found in the other five canonical books.  
      According to Fuʾād ʿAbdul Bāqī: 428 are Ṣaḥīḥ, 199 Ḥasan, 613 Ḍaʿīf, and 99 Munkar or Mawḍūʿ.  
      Shaykh Nāṣiruddīn al-Albānī identified 948 weak aḥādīth in his work <em>Ṣaḥīḥ wa Ḍaʿīf Sunan Ibn Mājah</em>.
    </p>

    <p>
      Ibn Mājah wrote no formal introduction, but it is evident he aimed to compile all possible narrations on fiqh issues,
      regardless of chain strength.  
      His Sunan includes more unique aḥādīth and fewer repetitions than the other five, making it both rich and distinct.
    </p>

    <h3 class="text-md font-semibold mt-6">Unique Qualities of Sunan Ibn Mājah</h3>
    <ul class="list-disc pl-6">
      <li>Written in a clear, elegant style with chapter titles aligned to fiqh topics.</li>
      <li>Well-ordered chapters with little repetition.</li>
      <li>Concise yet comprehensive regarding legal rulings.</li>
      <li>Identifies gharīb (unfamiliar) aḥādīth and classifies them in special chapters.</li>
      <li>Mentions the hometown of each narrator.</li>
      <li>Includes 482 authentic aḥādīth not found in the other five books.</li>
      <li>Contains 3,002 shared aḥādīth narrated through different chains, strengthening their authenticity.</li>
      <li>Has 1,339 unique additions (<em>Zawāʾid</em>), earning it its place as the sixth canonical book of Ḥadīth.</li>
    </ul>
  </section>
