  <p>
    Ṣaḥīḥ Muslim is a collection of hadīth compiled by Imām Muslim ibn al-Hajjāj al-Naysāburi (rahimahullāh).
    His collection is considered one of the most authentic collections of the Sunnah of the Prophet (ﷺ),
    and along with Ṣaḥīḥ al-Bukhārī forms the <strong>Sahihain</strong> — “The Two Sahihs.”
    It contains roughly 7,500 hadīth (with repetitions) in 57 books.
  </p>

  <p class="text-sm text-gray-500 italic">
    The translation provided here is by Abdul Hamid Siddiqui.
  </p>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">Author Bio</h2>
    <p>
      Imām Muslim’s full name is Abu al-Husayn Muslim ibn al-Hajjāj ibn Muslim ibn Warat al-Qushayri
      al-Naysaburi (206–261 AH / 821–875 AD). He belonged to the Qushayr tribe of the Arabs, an
      offshoot of the great clan of Rabi‘a.
    </p>

    <p>
      Born in Naysabur (Nishapur) in 206/821, his parents were righteous people who deeply influenced
      his character. Imām Muslim travelled widely to collect hadith in Arabia, Egypt, Syria, and Iraq,
      studying under great scholars such as Isḥāq b. Rāḥawayh, Aḥmad b. Hanbal, and Qutaiba b. Sa‘id.
    </p>

    <p>
      After completing his education, he settled in Nishapur and became a close student of
      Imām al-Bukhārī. He was also influenced by Muhammad ibn Yahya al-Dhuhali but later distanced
      himself due to a theological dispute, siding with Imām al-Bukhārī — showing his deep loyalty
      and respect.
    </p>

    <p>
      Imām Muslim authored several works, the most important being his <em>Musnad as-Ṣaḥīḥ</em>,
      written in response to a student’s request. He reportedly reviewed around 300,000 hadith,
      selecting only about 4,000 that met his strict authenticity criteria.
    </p>
  </section>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">Methods of Classification and Annotation</h2>
    <p>
      Imām Muslim adhered to rigorous principles in hadith verification, emphasizing unbroken and
      reliable chains of transmission in harmony with other trustworthy narrations. He divided
      narrators into three categories based on reliability and memory.
    </p>

    <ul class="list-disc pl-6">
      <li>Completely authentic narrators with perfect memory and integrity.</li>
      <li>Trustworthy narrators of slightly lesser precision, such as ‘Aṭā ibn Sa‘id and Layth ibn Abi Sulaim.</li>
      <li>Narrators of disputed honesty — these were excluded from his collection.</li>
    </ul>

    <p>
      Imām Muslim was meticulous with wording variations and modes of narration, distinguishing
      between <em>haddathana</em> (“he narrated to us”) and <em>akhbarana</em> (“he informed us”),
      reflecting whether the hadith was spoken or read to the teacher. He also ensured that each
      hadith was narrated by at least two reliable successors (<em>tābi‘īn</em>) from two Companions.
    </p>

    <p>
      His precision avoided confusion in narrator identification and ensured linguistic clarity,
      making <em>Sahih Muslim</em> a model of hadith methodology. It was later explained in detail
      by Imām an-Nawawi and his teacher Abu ‘Amr ibn Ṣalāḥ.
    </p>
  </section>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">His Students</h2>
    <p>
      Imām Muslim had a wide circle of students, many of whom became great scholars — including
      Abu Hatim Razi, Musa ibn Harun, Ahmad ibn Salama, Abu ‘Isa Tirmidhi, Abu Bakr ibn Khusaima,
      Abu ‘Awana, and Al-Dhahabi.
    </p>
  </section>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">His Death</h2>
    <p>
      Imām Muslim lived for 55 years, dedicating his life entirely to hadith collection, teaching,
      and preservation. He passed away in 261 AH (875 CE) and was buried in the suburbs of Nishapur.
    </p>
  </section>
