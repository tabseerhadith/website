  <p>
    <em>Sunan an-Nasāʾī</em> is a collection of ḥadīth compiled by Imām Aḥmad an-Nasāʾī (raḥimahullāh).  
    His collection is unanimously considered one of the six canonical ḥadīth books 
    (<em>Kutub as-Sittah</em>) of the Sunnah of the Prophet (ﷺ).  
    It contains approximately 5,700 ḥadīth (with repetitions) divided into 52 books.
  </p>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">Author Bio</h2>
    <p>
      Aḥmad ibn Shuʿayb ibn ʿAlī ibn Sīnān Abū ʿAbd ar-Raḥmān an-Nasāʾī 
      (214–303 AH / 829–915 CE) was born in the city of Nasā, in Khurāsān (Western Asia), 
      a renowned center of Islamic learning. He began his studies in his hometown, where 
      he specialized in ḥadīth. At age 20, he began travelling in pursuit of knowledge, 
      beginning with Qutaybah ibn Saʿīd. He journeyed through Iraq, Kūfa, Ḥijāz, Shām, 
      and finally settled in Egypt, which became his home and base for teaching.
    </p>
  </section>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">Memory, Piety, and Character</h2>
    <p>
      Imām an-Nasāʾī was known for his immense taqwā and exceptional memory.  
      The historian and scholar adh-Dhahabī described him as “the most knowledgeable 
      scholar of Egypt.” He followed the Sunnah closely — dressing with dignity, eating 
      simply yet healthily, and maintaining balance in worship and life.
    </p>

    <p>
      He would fast on alternate days, known as the fast of Dāwūd (ʿalayhi as-salām), 
      and spent his nights in worship and his days teaching ḥadīth.  
      He performed Ḥajj nearly every year and even participated in jihād.  
      His contemporaries described him as truthful, ascetic, and steadfast in righteousness.
    </p>
  </section>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">Teachers and Students</h2>
    <p>
      Among his notable teachers were Isḥāq ibn Rāhwayh, Imām Abū Dāwūd as-Sijistānī 
      (author of <em>Sunan Abī Dāwūd</em>), and Qutaybah ibn Saʿīd.  
      After settling in Egypt, Imām an-Nasāʾī became widely known as 
      <em>Ḥāfiẓ al-Ḥadīth</em> (The Preserver of Ḥadīth), attracting large circles of students.
    </p>

    <p>
      His prominent students included:
    </p>
    <ul class="list-disc pl-6">
      <li>Imām Abul-Qāsim aṭ-Ṭabarānī</li>
      <li>Imām Abū Bakr Aḥmad ibn Muḥammad (Ibn as-Sunnī)</li>
      <li>Shaykh ʿAlī ibn Ṭaḥāwī, son of the renowned ḥadīth scholar Imām Ṭaḥāwī</li>
    </ul>
    <p>It is also narrated that Imām Ṭaḥāwī himself learned from Imām an-Nasāʾī directly.</p>
  </section>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">Muqallid or Mujtahid</h2>
    <p>
      Scholars have differed regarding Imām an-Nasāʾī’s school of thought.  
      Many considered him a follower of the Shāfiʿī madhhab, while others, including Ibn Taymiyyah, 
      identified him as leaning towards the Ḥanbalī school.  
      Most likely, he was a <em>mujtahid</em> — an independent scholar — 
      whose juristic views were closest to Ḥanbalī principles but not confined to them.
    </p>
  </section>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">His Works</h2>
    <p>
      Imām an-Nasāʾī authored many significant works, though not all have survived.  
      His scholarship and precision in narration place him among the top tier of ḥadīth scholars, 
      equal in stature to Imām al-Bukhārī and Imām Ibn Ḥazm.
    </p>

    <p>His known works include:</p>
    <ol class="list-decimal pl-6">
      <li><em>As-Sunan al-Kubrā</em></li>
      <li><em>As-Sunan as-Sughrā</em> / <em>Al-Mujtabā</em> / <em>Al-Mujtanā</em> (popularly known as <em>Sunan an-Nasāʾī</em>)</li>
      <li><em>ʿAmal al-Yawm wa al-Laylah</em></li>
      <li><em>Kitāb ad-Ḍuʿafāʾ wal-Matrūkīn</em></li>
      <li><em>Khaṣāʾiṣ ʿAlī</em></li>
      <li><em>Al-Jarḥ wa at-Taʿdīl</em></li>
    </ol>

    <p>
      The most famous of these is <em>Sunan an-Nasāʾī</em>, which holds a place among the 
      <em>Kutub as-Sittah</em> (Six Canonical Collections) and is taught worldwide.
    </p>

    <p>
      After completing <em>As-Sunan al-Kubrā</em>, Imām an-Nasāʾī presented it to the governor of Ramla.  
      When asked if all narrations in it were authentic, he replied in the negative.  
      The governor then suggested he compile another work containing only authentic ḥadīths.  
      Thus, Imām an-Nasāʾī produced <em>As-Sunan as-Sughrā</em> — also known as <em>Al-Mujtabā</em> or <em>Al-Mujtanā</em>, 
      meaning “The Carefully Selected.” This is the version recognized today as <em>Sunan an-Nasāʾī</em>.
    </p>

    <p>
      In this book, he followed the methodology of Imām al-Bukhārī and Imām Muslim.  
      Most of its narrations are authentic, and when he included a weak report, he clearly 
      noted its weakness. For this reason, some scholars rank his work third in authenticity 
      after <em>Ṣaḥīḥ al-Bukhārī</em> and <em>Ṣaḥīḥ Muslim</em>.  
      Like Imām Muslim, he also clarifies linguistic nuances and presents variant narrations 
      where relevant.
    </p>
  </section>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">His Death</h2>
    <p>
      Towards the end of his life, political turmoil arose due to tensions with the Khawārij, 
      who cursed ʿAlī (raḍiyallāhu ʿanhu) and praised Muʿāwiyah (raḍiyallāhu ʿanhu).  
      Imām an-Nasāʾī’s public sermons defending ʿAlī led to his persecution and torture in 
      Palestine, forcing him to relocate to Egypt. However, harassment continued there as well.
    </p>

    <p>
      On his way to perform Ḥajj, he passed away in Makkah in 303 AH (≈ 915 CE) at the age of 88.  
      According to Ibn Ḥajar and adh-Dhahabī, he died in Ramla, Palestine, and his body was later 
      transferred to Makkah, where he was buried between Ṣafā and Marwah — near the Kaʿbah itself.
    </p>
  </section>
