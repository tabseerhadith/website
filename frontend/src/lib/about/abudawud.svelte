
<article class="prose prose-zinc dark:prose-invert leading-relaxed">
  <p>
    <em>Sunan Abī Dāwūd</em> is a collection of ḥadīth compiled by Imām Abū Dāwūd Sulaymān ibn al-Ashʿath
    as-Sijistānī (raḥimahullāh). It is widely considered among the six canonical collections of ḥadīth
    (<em>Kutub as-Sittah</em>) of the Sunnah of the Prophet (ﷺ). It consists of 5,274 aḥādīth in 43 books.
  </p>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">Author Bio</h2>
    <p>
      Abū Dāwūd Sulaymān ibn al-Ashʿath ibn Isḥāq ibn Bashīr ibn Shaddād ibn ʿAmr ibn ʿImrān al-Azdī
      as-Sijistānī was born in 202 AH in Sijistan — a province of Khurasan (modern-day Iran and Afghanistan).
      He was from the noble Azd tribe of Yemen.
    </p>
    <p>
      He began traveling to seek aḥādīth at a young age, reaching Baghdad in 220 AH when he was only 18.
      He meticulously verified the reliability of narrators before accepting their reports. His journeys
      in pursuit of knowledge took him across Khurasan, Iraq, Hijaz, Sham, Egypt, and Nishapur. In addition
      to being a muḥaddith, he was also a prominent faqīh who studied extensively under Imām Aḥmad ibn Ḥanbal.
    </p>
  </section>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">His Virtues</h2>
    <p>
      Imām an-Nawawī said: “All scholars are unanimous regarding the rank and virtue of Abū Dāwūd. He was
      praised for his perfect memory, deep knowledge, intellect, and piety — making him one of the greatest
      scholars of his time.”
    </p>
    <p>
      Adh-Dhahabī said: “Abū Dāwūd was among the greatest scholars. It has been said that Abū Dāwūd resembled
      Aḥmad ibn Ḥanbal in manner and conduct, who in turn resembled Wakīʿ, then Sufyān, then Manṣūr, then
      Ibrāhīm an-Nakhaʿī, then ʿAlqamah, then ʿAbdullāh ibn Masʿūd — who most resembled the Prophet (ﷺ) in
      character and behavior.”
    </p>
    <p>
      A beautiful story illustrates his humility: once while aboard a ship, Abū Dāwūd heard someone on shore
      sneeze and say “Alḥamdulillāh.” He hired a small boat for one dirham, rowed ashore, and said “Yarḥamukallāh”
      to the man. When asked why, he replied, “Perhaps that man’s supplication is accepted by Allah.” That night,
      people on the ship heard a voice saying, “O people of the ship, Abū Dāwūd has purchased his Jannah for one
      dirham.”
    </p>
    <p>
      Imām Abū Dāwūd (raḥimahullāh) passed away on Friday, 16 Shawwāl 275 AH, at the age of 73, and was buried
      in Basrah beside Sufyān ath-Thawrī.
    </p>
  </section>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">Teachers and Students</h2>
    <p>
      He studied under around 300 great scholars, including Imām Aḥmad ibn Ḥanbal, Musaddad ibn Musarhad,
      Yaḥyā ibn Maʿīn, Qutaibah ibn Saʿīd, Abū Bakr ibn Abī Shaibah, ʿUthmān ibn Abī Shaibah, ʿAlī ibn al-Madīnī,
      ʿAbdullāh ibn Maslamah al-Qaʿnabī, Sulaymān ibn Ḥarb, Abul Walīd aṭ-Ṭayālisī, Isḥāq ibn Ibrāhīm,
      Muslim ibn Ibrāhīm, and Abū Jaʿfar an-Nufailī.
    </p>
    <p>
      Among his renowned students were his son Abū Bakr ʿAbdullāh ibn Abī Dāwūd, Imām at-Tirmidhī,
      Imām an-Nasā’ī, Abū ʿAlī Muḥammad ibn Aḥmad al-Lu’lu’ī, Abū Saʿīd Aḥmad ibn Muḥammad ibn al-Aʿrābī,
      Abū Bakr Muḥammad ibn Bakr ibn Dāsah, and Imām Aḥmad ibn Ḥanbal — who was both his teacher and,
      in one narration, his student.
    </p>
  </section>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">The Sunan</h2>
    <p>
      When Imām Abū Dāwūd compiled his <em>Sunan</em>, Ibrāhīm al-Ḥarbi remarked:
      “Ḥadīth was made as supple for Abū Dāwūd as iron was made for Prophet Dāwūd (عليه السلام).”
      Ibn al-Aʿrābī said: “If a man possessed only the Book of Allah and this Sunan of Abū Dāwūd,
      he would need nothing else.”
    </p>
    <p>
      Imām Abū Dāwūd included only aḥādīth related to fiqh (jurisprudence), usually one or two per topic,
      for clarity and conciseness. He sometimes summarized long narrations to focus on legal rulings.
      He excluded narrations from those considered <em>matrūk al-ḥadīth</em> (rejected narrators) and
      marked <em>munkar</em> (defective) or weak narrations clearly, using them only when no stronger
      alternatives existed.
    </p>
    <p>
      According to his student Ibn Dāsah, Imām Abū Dāwūd said:
      “I have written 500,000 aḥādīth of Allah’s Messenger (ﷺ), from which I selected 4,800 for this book.
      Four aḥādīth alone are sufficient to preserve one’s Dīn.”
    </p>

    <ol class="list-decimal pl-6">
      <li>“Actions are judged according to intentions.”</li>
      <li>“Of the beauty of a person’s Islam is leaving what does not concern him.”</li>
      <li>“None of you truly believes until he loves for his brother what he loves for himself.”</li>
      <li>“The ḥalāl is clear and the ḥarām is clear, and between them are doubtful matters; whoever avoids them safeguards his Dīn.”</li>
    </ol>
  </section>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">His Other Works</h2>
    <p>
      Beyond his <em>Sunan</em>, Imām Abū Dāwūd authored several valuable works, including
      his <a href="/abudawud/letter" class="text-blue-600 hover:underline">Letter to the People of Makkah</a>,
      explaining the principles he followed in compiling his <em>Sunan</em>. Other notable works include:
      <em>Masā’il al-Imām Aḥmad</em>, <em>At-Tafarrud</em>, <em>Al-Marāsīl</em>, <em>Aʿlām an-Nubuwwah</em>,
      <em>Az-Zuhd</em>, and <em>An-Nāsikh wal-Mansūkh</em>.
    </p>
  </section>
