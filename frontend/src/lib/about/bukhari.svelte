  <p>
    <em>Ṣaḥīḥ al-Bukhārī</em> is a collection of ḥadīth compiled by Imām Abū ʿAbdullāh Muḥammad ibn Ismāʿīl
    al-Bukhārī (raḥimahullāh). His compilation is recognized by the overwhelming majority of the Muslim world
    as one of the most authentic collections of the Sunnah of the Prophet (ﷺ). It contains roughly 7,563
    ḥadīth (with repetitions) divided into 98 books.
  </p>

  <p class="text-sm text-gray-600 italic">
    The translation provided here is by Dr. M. Muhsin Khan.
  </p>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">Author Bio</h2>
    <p>
      Imām al-Bukhārī (raḥimahullāh) is known as <em>Amīr al-Muʾminīn fī al-Ḥadīth</em>.
      His full genealogy is: Abū ʿAbdullāh Muḥammad ibn Ismāʿīl ibn Ibrāhīm ibn al-Mughīrah ibn Bardizbah
      al-Bukhārī. His father, Ismāʿīl, was a respected muḥaddith who studied under Imām Mālik,
      Ḥammād ibn Zayd, and ʿAbdullāh ibn al-Mubārak (raḥimahumullāh).
    </p>

    <p>
      Imām al-Bukhārī was born on a Friday, the 13th of Shawwāl, 194 AH. His father passed away while he
      was still a child. At the age of sixteen, after memorizing the compilations of Wakīʿ and
      ʿAbdullāh ibn al-Mubārak, he performed Ḥajj with his elder brother and mother. After completing Ḥajj,
      he stayed in Makkah for two more years and later traveled to Madīnah, where he compiled
      <em>Qaḍāyā aṣ-Ṣaḥābah wa at-Tābiʿīn</em> and <em>Tārīkh al-Kabīr</em>. He later traveled extensively to
      centers of learning across Syria, Egypt, Kūfa, Baṣra, and Baghdād.
    </p>

    <p>
      He began studying ḥadīth in 205 AH, and by 210 AH started his long journeys in pursuit of knowledge.
      His memory was extraordinary — he could recall ḥadīths word-for-word after hearing them once. It is
      reported that by childhood he had memorized around 2,000 ḥadīths.
    </p>

    <p>
      Among his many works, his <em>Ṣaḥīḥ</em> stands as the most authoritative. Its full title is
      <em>Al-Jāmiʿ al-Musnad aṣ-Ṣaḥīḥ al-Mukhtaṣar min Umūr Rasūlillāh ﷺ wa Sunanihī wa Ayyāmihī</em>.
      He presented it to his teachers — Imām Aḥmad ibn Ḥanbal, Ibn al-Madīnī, and Ibn Maʿīn — for
      review. It took him sixteen years to compile, beginning around 217 AH, when he was only twenty-three.
    </p>

    <p>
      Before writing any ḥadīth into his compilation, he would perform ghusl and pray two rakaʿāt,
      seeking Allah’s guidance. Each narration was finalized in the Rawḍah of Masjid an-Nabawī —
      between the Prophet’s ﷺ grave and his minbar — and only then included in his collection.
    </p>
  </section>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">Methods of Classification and Annotation</h2>
    <p>
      Imām al-Bukhārī (raḥimahullāh) imposed strict conditions for acceptance of ḥadīth.
      Each narrator in the chain had to meet the following criteria:
    </p>

    <ul class="list-disc pl-6">
      <li>All narrators must be upright (<em>ʿadl</em>).</li>
      <li>
        Each must have strong memory, verified by other major muḥaddithīn in both memorization and
        accuracy of narration.
      </li>
      <li>The chain of transmission must be continuous with no missing narrators.</li>
      <li>
        It must be known that consecutive narrators in the chain actually met each other — a unique
        additional condition required by Imām al-Bukhārī.
      </li>
    </ul>

    <p>
      Imām an-Nawawī (raḥimahullāh) reported that the scholars of Islām unanimously agreed that
      <em>Ṣaḥīḥ al-Bukhārī</em> holds the highest level of authenticity after the Qurʾān.
      It contains 7,563 ḥadīths including repetitions; without repetitions, approximately 2,600.
    </p>
  </section>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">His Students</h2>
    <p>
      In 250 AH (864 CE), Imām al-Bukhārī settled in Naysābūr, where he met Imām Muslim ibn al-Ḥajjāj,
      who became one of his foremost students. Imām Muslim later authored <em>Ṣaḥīḥ Muslim</em>,
      regarded as the second most authentic ḥadīth collection after al-Bukhārī’s.
    </p>
  </section>

  <section class="mt-10">
    <h2 class="text-lg font-semibold mb-2">His Death</h2>
    <p>
      Political disputes forced Imām al-Bukhārī to leave Naysābūr and move to Khartank,
      a village near Samarkand, where he passed away in 256 AH / 870 CE.
    </p>
  </section>
