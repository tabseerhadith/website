  <h2 class="text-base font-semibold">Footnotes</h2>
  <ol class="list-decimal pl-6 text-sm">
    <li><em>Bāb</em>: a chapter or topic heading.</li>
    <li><em>Mursal</em>: a ḥadīth narrated by a Tābi‘ī directly from the Prophet ﷺ without mentioning a Companion.</li>
    <li><em>Musnad</em>: with a connected chain of transmission.</li>
    <li><em>Muttasil</em>: connected (unbroken isnād).</li>
    <li><em>Matrūk</em>: a narrator whose reports are abandoned and not used even for support.</li>
    <li><em>Munkar</em>: rejected or clearly mistaken narration.</li>
    <li><em>Wāhī</em>: extremely weak.</li>
    <li><em>Ṣāliḥ</em>: acceptable; not severely weak.</li>
    <li><em>Jawāmiʿ</em>: comprehensive collections covering various topics.</li>
    <li><em>Mashhūr</em>: famous, well-known narrations.</li>
    <li><em>Mudallas</em>: narration with hidden discontinuity (ambiguous transmission).</li>
    <li><em>Marāsīl</em>: plural of mursal.</li>
  </ol>

