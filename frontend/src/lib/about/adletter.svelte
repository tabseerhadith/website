  <h1 class="text-xl font-semibold text-center mb-6">
    The Letter of Imām Abū Dāwūd as-Sijistānī to the People of Makkah Describing His <em>Sunan</em>
  </h1>

  <p class="italic text-sm text-gray-600">
    Narrated by Abul-Ḥusayn ibn Jumay‘ from Muḥammad ibn ‘Abdil-‘Azīz al-Hāshimī from him:
  </p>

  <blockquote class="text-center not-italic my-6">
    <p><strong>In the Name of Allah, ar-Raḥmān, ar-Raḥīm</strong><br>And there is no might or power except by Allah.</p>
  </blockquote>

  <p class="italic">
    Shaykh Abul-Fatḥ Muḥammad ibn ‘Abdil-Bāqī ibn Aḥmad ibn Sulaymān (known as Ibn al-Bāṭī) informed us by way of ijāzah.
    Shaykh Abul-Faḍl Aḥmad ibn al-Ḥasan ibn Khayrūn al-Mu‘addal reported to us, by way of reading to him while I was present.
    It was said to him: “You had it read upon Abū ‘Abdillah Muḥammad ibn ‘Alī ibn ‘Abdillah as-Sūrī al-Ḥāfiẓ,”
    who said: “I heard Abul-Ḥusayn Muḥammad ibn Aḥmad ibn Muḥammad ibn Aḥmad ibn Jumay‘ al-Ghassānī at Ṣaydā,
    who confirmed it, saying: I heard Abū Bakr Muḥammad ibn ‘Abdil-‘Azīz ibn Muḥammad ibn al-Faḍl ibn Yaḥyā
    ibn al-Qāsim ibn ‘Awn ibn ‘Abdillah ibn al-Ḥārith ibn Nawfal ibn al-Ḥārith ibn ‘Abdil-Muṭṭalib al-Hāshimī
    at Makkah, saying:
  </p>

  <p class="italic">
    “I heard Abū Dāwūd Sulaymān ibn al-Ash‘ath as-Sijistānī at al-Baṣrah. He was asked about his letter
    which he wrote to the people of Makkah and others, so he dictated to us:”
  </p>

  <hr class="my-6 border-gray-300 dark:border-gray-700" />

  <p>
    <strong>Peace be upon you.</strong> I extol and praise Allah to you, the One whom there is no deity worthy of worship except Him.
    I ask Him to send ṣalāh upon Muḥammad, His slave and Messenger — may Allah’s peace and blessings be upon him whenever he is mentioned.
  </p>

  <p>
    May Allah grant us a pardon unaccompanied by anything displeasing and after which there shall be no punishment.
    Indeed, you asked me regarding the ḥadīths in <em>Kitāb as-Sunan</em> — whether they are the most authentic I know concerning each topic.
    I examined all that you mentioned.
  </p>

  <p>
    Know that this is indeed the case, except in rare instances where two authentic routes exist, one with a better chain
    while the narrator of the other has stronger memory. I may include both; however, such cases do not exceed ten in my book.
  </p>

  <p>
    I wrote only one or two ḥadīths concerning each <em>bāb</em><sup>1</sup>, even if more existed,
    in order to make the book easy to benefit from. When I repeat a ḥadīth, it is only due to additional wording or benefit.
  </p>

  <p>
    Occasionally I summarized long ḥadīths so that listeners would understand the legal point (<em>fiqh</em>)
    without being distracted by lengthy narration.
  </p>

  <p>
    As for <em>mursal</em><sup>2</sup> ḥadīths, early scholars such as Sufyān ath-Thawrī, Mālik ibn Anas,
    and al-Awzā‘ī used them as proof until ash-Shāfi‘ī criticized them, and Aḥmad ibn Ḥanbal followed him.
    Thus, when there is no <em>musnad</em><sup>3</sup> ḥadīth opposing a <em>mursal</em> one, the latter may be used as evidence,
    though it is weaker than a <em>muttasil</em><sup>4</sup> report.
  </p>

  <p>
    My <em>Sunan</em> contains no narrations from those who are <em>matrūk al-ḥadīth</em><sup>5</sup>.
    When I included a <em>munkar</em><sup>6</sup> ḥadīth, I clarified its status.
  </p>

  <p>
    Many ḥadīths in this work are drawn from Imām Mālik’s <em>Muwaṭṭaʾ</em>,
    the compilations of Ḥammād ibn Salamah and ʿAbdur-Razzāq.
    About one-third of these ḥadīths are not found in those collections.
  </p>

  <p>
    If a Sunnah of the Prophet ﷺ reaches you that I have not reported, know that it is likely an extremely weak (<em>wāhī</em><sup>7</sup>)
    report — except if it occurs elsewhere in my book by another chain.
  </p>

  <p>
    I know of no one who gathered ḥadīths in a comprehensive way other than myself.
    Al-Ḥasan ibn ʿAlī al-Khallāl compiled about nine hundred, while Ibn al-Mubārak estimated
    that the Sunan of the Prophet ﷺ number about nine hundred. Abū Yūsuf said they are eleven hundred.
  </p>

  <p>
    Any ḥadīth in my book that contains severe weakness I have explained.
    Whatever I did not comment upon is <em>ṣāliḥ</em><sup>8</sup> (acceptable), though some are stronger than others.
  </p>

  <p>
    You will not find any authentic Sunnah from the Prophet ﷺ with a sound chain except that it is in this book.
    I know of nothing after the Qur’an more essential for the people to learn than this work.
    Whoever studies it and reflects will know its value.
  </p>

  <p>
    I encourage one who studies it to also record the opinions of the Companions, as well as works like
    the <em>Jāmiʿ</em> of Sufyān ath-Thawrī<sup>9</sup>, the best of what has been compiled of <em>Jawāmiʿ</em>.
  </p>

  <p>
    Most of the ḥadīths in my book are well-known (<em>mashāhīr</em><sup>10</sup>), though not everyone can distinguish them.
    The truly remarkable aspect of this collection is its reliance on famous reports,
    for a strange (<em>gharīb</em>) ḥadīth cannot be used as proof, even if narrated by Imāms like Mālik or Yaḥyā ibn Saʿīd.
  </p>

  <p>
    There are a few ḥadīths that are <em>mursal</em> or <em>mudallas</em><sup>12</sup>,
    when no fully connected authentic report could be found.
    Such cases are rare in <em>Kitāb as-Sunan</em>.
  </p>

  <p>
    Occasionally, I omitted a ḥadīth when I did not fully understand it or found its authenticity uncertain.
    Revealing every deficiency to the common people can be harmful, as not all have the knowledge to assess it.
  </p>

  <p>
    The total number of books in the <em>Sunan</em> is eighteen <em>juzʾ</em>, one of which consists of <em>marāsīl</em><sup>13</sup>.
    The book contains approximately 4,800 ḥadīths and about 600 <em>mursal</em> ones, all concerning rulings (<em>aḥkām</em>).
  </p>

  <p>
    And peace be upon you, and the mercy of Allah, and His blessings.
    May Allah send ṣalāh upon our master Muḥammad, the Prophet, and upon his family and companions.
  </p>

